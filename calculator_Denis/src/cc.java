
public class cc {

	public static void main(String[] args) {
		
		int num1 = 562;
		double num2 = 33;
		double result;
		
		result = num1 + num2;
		System.out.println("562 plus 33 is " + result);
		
		result = num1 - num2;
		System.out.print("562 minus 33 is ");
		System.out.println (result);

		result = num1 / num2;
		System.out.print("562 divided 33 is ");
		System.out.println (result);

		result = num1 * num2;
		System.out.print("562 multiplied by 33 is ");
		System.out.println (result);

		result = num1 % num2;
		System.out.print("The remainder of 562 ");
		System.out.print("divided by 33 is ");
		System.out.println (result);
	
		result++;
		
		System.out.print("Incremented Result is ");
		System.out.println(result);
		
	
	}
	

}
