

public class cc2 {

	public static void main(String [] args) {

		 int a = 35;
		 System.out.println(a &= 3);
		
		int b = 40;
		System.out.println(b ^= 3);
		
		 int c = 45;
		 System.out.println(c |= 3);

		 int d = 50;
		 System.out.println(d <<= 3);
		 
		 int e = 55;
		 System.out.println(e >>= 3);
		 
		 int f = 60;
		 System.out.println(f >>>= 3);
		 
	}

}
